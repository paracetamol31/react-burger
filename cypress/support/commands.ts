Cypress.Commands.add("testCard", (cardName: string)=> {
    cy.get("[data-test=burger-constructor]").as("burgerConstructor");

    cy.get("[data-test=ingredient–°ard]").contains(cardName).as("card");
    cy.get("@card").click();
    cy.get("[data-test=modal-window-close-icon]").click();
    cy.get("@card").trigger("dragstart");
    cy.get("@burgerConstructor").trigger("drop");
    cy.get("@card").trigger("dragend");
})